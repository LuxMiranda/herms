module Lang.Spanish where

import Lang.Strings

spanish :: String -> String
spanish str
  | is tuiTitle       = "                                    Herm's - Agregar una receta"
  | is tuiName        = "        Nombre: "
  | is tuiDesc        = "   Descripción: "
  | is tuiServingSize = "     Porciones: "
  | is tuiHeaders     = "                  ctd.   unidad             nombre              atributo"
  | is tuiIngrs       = "  Ingredientes: \n(uno por línea)  "
  | is tuiDirs        = " Instrucciones: \n(una por línea)  "
  | is tuiTags        = "     Etiquetas: "
  | is tuiHelp1       = "                      Tab / Shift+Tab           - Siguiente / Campo anterior"
  | is tuiHelp2       = "                      Ctrl + <flechas>          - Navegar campos"
  | is tuiHelp3       = "                      [Meta o Alt] + <h-j-k-l>  - Navegar campos"
  | is tuiHelp4       = "                      Esc                       - Guardar o Cancelar"
  | is headerServs    = "\nPorciones: "
  | is headerIngrs    = "\nIngredientes:\n"
  | is saveRecipeYesNoEdit = "¿Guardar la receta? (S)í  (N)o (E)ditar"
  | is y              = "s"
  | is yCap           = "S"
  | is n              = "n"
  | is nCap           = "N"
  | is e              = "e"
  | is eCap           = "E"
  | is recipeSaved    = "¡Receta guardada!"
  | is changesDiscarded = "Cambios descartados."
  | is badEntry         = "Por favor, ingrese ÚNICAMENTE 's', 'n' o 'e'"
  | is doesNotExist     = " no existe\n"
  | is nothingToImport  = "Nada para importar"
  | is importedRecipes  = "Recetas importadas:"
  | is metric         = "métrico"
  | is imperial       = "imperial"
  | is more           = " [más]"
  | is capTags        = "Etiquetas"
  | is removingRecipe = "Eliminando receta: "
  | is group          = "grupo"
  | is [groupShort]   = "g"
  | is groupDesc      = "agrupar recetas por etiquetas"
  | is nameOnly       = "solo-nombre"
  | is [nameOnlyShort] = "n"
  | is nameOnlyDesc   = "mostrar únicamente los nombres de las recetas"
  | is tags           = "etiquetas"
  | is tagsMetavar    = "ETIQUETAS"
  | is tagsDesc       = "mostrar recetas con etiquetas específicas"
  | is serving        = "porción"
  | is [servingShort] = "p"
  | is servingDesc    = "especificar tamaño de la porción al visualizar"
  | is servingMetavar = "INT"
  | is step           = "paso"
  | is [stepShort]    = "p"
  | is stepDesc       = "Si se visualiza paso a paso"
  | is convert        = "convertir"
  | is [convertShort] = "c"
  | is convertDesc    = "Convierte las unidades de la receta a métrico o imperial."
  | is convertMetavar = "CONV_UNIT"
  | is none           = "Ninguno/a"
  | is recipeNameMetavar = "NOMBRE_RECETA"
  | is recipeNameDesc    = "índice o nombre de la receta"
  | is fileNameMetavar   = "NOMBRE_ARCHIVO"
  | is fileNameDesc      = "nombre del archivo"
  | is list           = "lista"
  | is listDesc       = "listar recetas"
  | is view           = "visualizar"
  | is viewDesc       = "visualizar las recetas específicas"
  | is add            = "agregar"
  | is addDesc        = "agregar una nueva receta (interactivamente)"
  | is edit           = "editar"
  | is editDesc       = "editar un receta"
  | is import'        = "importar"
  | is importDesc     = "importar un archivo de receta"
  | is export         = "exportar"
  | is exportDesc     = "exportar recetas a stdout"
  | is remove         = "eliminar"
  | is removeDesc     = "eliminar las recetas específicas"
  | is shopping       = "compras"
  | is shoppingDesc   = "generar una lista de compras para las recetas especificadas"
  | is datadir        = "datadir"
  | is datadirDesc    = "mostrar la ubicación de los archivos de receta y configuración"
  | is version        = "versión"
  | is [versionShort] = "v"
  | is versionDesc    = "Visualizar versión"
  | is progDesc       = "HeRM's: Un administrador de recetas en Haskell. Escribir \"herms --help\" para ver las opciones"
  | is find           = "encontrar"
  | is findDesc       = "imprime las recetas describen por una expresión regular (semejante a grep)"
  | otherwise         = str

  where is = (str ==)
